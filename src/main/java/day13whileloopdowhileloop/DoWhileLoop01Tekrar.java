package day13whileloopdowhileloop;

public class DoWhileLoop01Tekrar {

    public static void main(String[] args) {

        int i=0;

        while (i<1){
            System.out.println("Sen bir while loop'sun");
            i++;
        }

        int k = 0;

        do {
            k++;
            System.out.println("Sen bir while loop'sun");
        }while (k<1);














    }








}
